   <p>Parties wishing to divorce may use a mediator prior to initiating a dissolution with the court or at any point during the dissolution process. Instead of the traditionally adversarial process of a court battle, mediation offers the parties a chance to communicate and negotiate with each other with the goal of coming to a resolution that is acceptable to both sides. Mediation offers the parties the ability to avoid the very expensive and lengthy court process and end their marriage relationship amicably. Contact our office today for more information and speak with a Qualified Neutral Family Mediator.</p>
