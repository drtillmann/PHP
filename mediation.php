   <h2><br />
     Civil Mediation</h2>
   <p><br />
     Are you currently involved in a dispute, whether large or small, with another individual or organization? The goal of civil mediation is to solve these conflicts and avoid the time and expense of going to court. You do not have to initiate a lawsuit in order to engage in civil mediation. However, if you have initiated a lawsuit or already are a party to a lawsuit, courts strongly encourage mediation throughout the litigation process.</p>
<p>Mediation creates the ability for the parties to communicate and negotiate with one another to create a settlement agreement that is agreeable to both sides rather than leaving the entire matter up to a judge or jury. Not only does mediation cost a fraction of a court trial, but it also allows the parties to resolve things quickly and amicably. Contact our office today to speak with a Qualified Neutral Civil Mediator.</p>
